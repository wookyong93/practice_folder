package ex02;

public class OutExam03 {
	public static void main(String[] args) {
		System.out.printf( "\n");
		// \n : 한칸내림
		System.out.println("A\tB");
		// 이클립스는 탭 간격이 4칸 
		// 윈도우 탭	간격은 8칸
		System.out.println("이름\t: ");
		System.out.println("몸무게\t: ");
		System.out.println("abcd\t:");
		System.out.println("abcdefgh:");
		
		
		System.out.println("abcd\refg");
		// \r 라인의 제일 처음으로 이동한다.
		System.out.println("abcd\nefg");
		
		System.out.println("abcd\befg");
		// abcefg
		
		
	}
}
