package ex01;

public class IfExam02 {
	public static void main(String[] args) {
		//if ~ else  는 조건 하나의 두가지의 명령을 선택해서 쓸 수 있다.
		//else 에는 조건을 넣을 수 없다.
		int var = 10;
		
		if(var%2 == 0) {
			System.out.println("변수의 값은 짝수입니다.");
		} else//기타 등..
		{ 
			System.out.println("변수의 값은 홀수입니다.");
		}

		
			
	}
}
