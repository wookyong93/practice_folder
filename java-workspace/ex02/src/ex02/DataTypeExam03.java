package ex02;

public class DataTypeExam03 {
	public static void main(String[] args) {
		char ch1 = 'A';
		// '' 를 이용하면 단일문자 표현
		// "" 를 이용하면 문자열 표현
		 System.out.println(ch1);
		 char ch2 = 65;
		 System.out.println(ch2);
		 
		 char ch3 = 0x0041;
		 System.out.println(ch3);
		 
		 char ch4 = '\u0041';
		 System.out.println(ch4);
		 
	}
}
